    {
        $order = Order::find($order_id);
        $pizza = Pizza::find($pizza_id);
        $qty = $request->input('qty');


        Order::find($order_id)->pizzas()->attach($pizza,['qty'=> $qty]);

        return view('pizza',['order'=>$order, 'pizzas'=>Pizza::all()]);
    }

